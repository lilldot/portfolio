      <h5 class="featurette-heading"><strong>07 Post Launch User Testing</strong>
          <span>Understanding the User Journey</span>
      </h5>
      <p>To further improve the product and to reduce operation cost (after a year of testing the feature in the wild),
        I conducted 9 user interviews with employers who recently went through the renewal flow to learn about their experience.
        I gathered the top insights and created actionable proposals to better serve the needs of our users.</p>
    </div>
    <div class="medium-6 large-3 columns">
      <ul class='example'>
        <li><h6><strong>e.g.</strong></h6></li>
        <li><strong>Insight 1</strong>: 4 of 9 users had issues with the communication process and found the email threads either confusing or left unanswered</li>
        <li><strong>Recommendation 1</strong>: providing a phone number and personal guidance is essential for users with specific questions in mind</li>
        <li><strong>Insight 2</strong>: 6 of 9 users want to see more granular options in the survey to get more tailored reccomendations</li>
        <li><strong>Recommendation 2</strong>:  provide a set of curated options for users to choose what’s most important for them</li>
      </ul>
